package JAVA_LOGICAL;

public class Array2D_program
{
    public static void main(String[] args)
    {
        int a=2;
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=6;j++)
            {
                System.out.print(a*j+" ");
            }
            a++;
            System.out.println();
        }

    }

}
